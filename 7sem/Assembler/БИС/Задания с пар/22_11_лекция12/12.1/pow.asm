.MODEL TINY
.CODE
.STARTUP
ORG 100H
	INT 16H
	SUB AL,'0'
	MUL AL
	CMP AL,16
	JG PRINT_RESULT
	MUL AL

PRINT_RESULT:
	MOV BX,2660

    CMP AX, 0
    JE FINISH

PRINT_SYMBOL:
	DIV BL
    CMP AL, 0
    JE SKIP_ZERO
FINISH:
	ADD AL,'0'
	INT 29H
SKIP_ZERO:
	MOV AL,AH
	MUL BH
	CMP AL,0
	JNE PRINT_SYMBOL

	MOV AH,4CH
	INT 21H
END
